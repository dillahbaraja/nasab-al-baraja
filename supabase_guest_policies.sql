-- Public can create pending child suggestions only
create policy "nodes_insert_pending_public"
on public.nodes
for insert
to anon, authenticated
with check (
  coalesce(moderation->>'status', '') = 'pending'
  and coalesce(moderation->>'type', '') = 'add_child'
);

-- Public can update rows that are already pending
create policy "nodes_update_pending_public"
on public.nodes
for update
to anon, authenticated
using (
  coalesce(moderation->>'status', '') = 'pending'
)
with check (
  coalesce(moderation->>'status', '') = 'pending'
  and (
    coalesce(moderation->>'type', '') = 'add_child'
    or moderation ? 'nameChange'
  )
);

-- Public can add/edit pending name change on a normal node
create policy "nodes_update_name_change_public"
on public.nodes
for update
to anon, authenticated
using (true)
with check (
  moderation ? 'nameChange'
  and coalesce(moderation->'nameChange'->>'status', '') = 'pending'
);

-- Public can cancel/delete only pending suggestion rows
create policy "nodes_delete_pending_public"
on public.nodes
for delete
to anon, authenticated
using (
  coalesce(moderation->>'status', '') = 'pending'
);


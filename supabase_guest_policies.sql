create or replace function public.touch_baraja_member_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create or replace function public.enforce_baraja_member_update_guard()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if auth.uid() = old.auth_user_id and not public.can_manage_baraja_members() then
    if new.person_id <> old.person_id
      or new.auth_user_id <> old.auth_user_id
      or new.claim_status <> old.claim_status
      or new.member_level <> old.member_level
      or coalesce(new.approved_by_member_id, -1) <> coalesce(old.approved_by_member_id, -1)
      or coalesce(new.approved_at, timestamptz 'epoch') <> coalesce(old.approved_at, timestamptz 'epoch')
      or new.arabic_name_snapshot <> old.arabic_name_snapshot
      or new.english_name_snapshot <> old.english_name_snapshot
      or new.email <> old.email then
      raise exception 'You can only update your own profile fields.';
    end if;
  end if;

  return new;
end;
$$;


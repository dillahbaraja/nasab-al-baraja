create or replace function public.is_admin_user()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admin_users
    where email = auth.jwt() ->> 'email'
  )
  or exists (
    select 1
    from public.baraja_member
    where auth_user_id = auth.uid()
      and claim_status = 'approved'
      and member_level = 'admin'
  );
$$;

create or replace function public.current_baraja_member_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select id
  from public.baraja_member
  where auth_user_id = auth.uid()
  order by id asc
  limit 1;
$$;


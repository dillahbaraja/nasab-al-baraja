create or replace view public.baraja_member_public_status as
select
  person_id,
  case
    when bool_or(claim_status = 'approved') then 'approved'
    when bool_or(claim_status = 'pending') then 'pending'
    else 'none'
  end as claim_status
from public.baraja_member
where claim_status in ('pending', 'approved')
group by person_id;

grant select on public.baraja_member_public_status to anon, authenticated;

create or replace function public.approve_baraja_member_claim(target_member_id bigint)
returns public.baraja_member
language plpgsql
security definer
set search_path = public
as $$
declare
  target_row public.baraja_member;
  approver_id bigint;
begin
  if not public.can_manage_baraja_members() then
    raise exception 'Only verified members or admins can approve claims.';
  end if;

  approver_id := public.current_baraja_member_id();

  select *
  into target_row
  from public.baraja_member
  where id = target_member_id
  for update;

  if not found then
    raise exception 'Target member claim was not found.';
  end if;

  if target_row.claim_status <> 'pending' then
    raise exception 'Only pending claims can be approved.';
  end if;

  if exists (
    select 1
    from public.baraja_member
    where person_id = target_row.person_id
      and claim_status = 'approved'
      and id <> target_member_id
  ) then
    raise exception 'This person is already connected to another approved member.';
  end if;

  update public.baraja_member
  set
    claim_status = 'approved',
    member_level = case when member_level = 'admin' then 'admin' else 'verified' end,
    approved_at = now(),
    approved_by_member_id = approver_id
  where id = target_member_id
  returning * into target_row;

  return target_row;
end;
$$;

create or replace function public.promote_baraja_member_admin(target_member_id bigint)
returns public.baraja_member
language plpgsql
security definer
set search_path = public
as $$
declare
  target_row public.baraja_member;
begin
  if not public.is_admin_baraja_member() then
    raise exception 'Only admins can promote another member.';
  end if;

  update public.baraja_member
  set member_level = 'admin'
  where id = target_member_id
    and claim_status = 'approved'
    and member_level in ('verified', 'admin')
  returning * into target_row;

  if not found then
    raise exception 'Target member must be an approved verified member.';
  end if;

  return target_row;
end;
$$;

